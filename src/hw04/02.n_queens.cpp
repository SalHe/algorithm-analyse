#define DOCTEST_CONFIG_IMPLEMENT_WITH_MAIN
#include <doctest.h>
#include <vector>
#include <functional>

using namespace std;

void nQueens(int size, int currRow,
             vector<vector<int>> &solutions,
             vector<int> &placements,
             vector<bool> &columns, vector<bool> &diagonal, vector<bool> &diagonal2)
{
    if (currRow == size)
    {
        solutions.push_back(placements);
        return;
    }

    for (int col = 0; col < size; col++)
    {
        int d1 = col - currRow + size - 1;
        int d2 = col + currRow;
        if (columns[col] || diagonal[d1] || diagonal2[d2])
        {
            continue;
        }

        placements[currRow] = col;
        columns[col] = diagonal[d1] = diagonal2[d2] = true;

        nQueens(size, currRow + 1, solutions, placements, columns, diagonal, diagonal2);

        placements[currRow] = -1; // 非必须的，只是为了方便调试查看
        columns[col] = diagonal[d1] = diagonal2[d2] = false;
    }
}

vector<vector<int>> nQueens(int size)
{
    vector<vector<int>> solutions;
    vector<int> placements(size, -1);

    // 用于标记某一列、某一斜线、某一反斜线上已放置棋子
    vector<bool> columns(size, false);
    vector<bool> diagonal(2 * size, false);
    vector<bool> diagonal2(2 * size, false);

    nQueens(size, 0, solutions, placements, columns, diagonal, diagonal2);

    return solutions;
}

